# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# Só existe duas respostas possiveis:
# sim (true) ou não (false.)
# Existem varios operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro 
# Expressões com dois sinais de igual (==), conferem se um valor é igual a outro valor e se eles são do mesmo tipo.
print(10 == 10) # Nesta linha estou perguntando: 10 é igual (==) a 10. Resposta: Sim  => True (Verdadeiro)
print(10 == 11) # Resposta:Não => False (Falso)

# O tipo de dado Boolean (bool) é só pra você questionar a respeito de True ou False
print(type(10 == 10))
print(type(10 == 11))

