# Tipo int e float
# int -> Numeros inteiros
# O tipo "int" representa qualquer numero positivo ou negativo. 'int' sem o sinal de negativo "-" é considerado positivo.
#print(11) # int
#print(-11) # int
#print(0) # int

# float -> Numeros com ponto flutuante (representados com ponto final ao invés de virgula)
# O tipo float representa qualquer número positivo ou negativo com ponto flutuante. 'float' sem o sinal de negativo "-" é considerado positivo.
#print(1.1, 10.11)
#print(0.0, -1.5)

# A função type mostra o tipo que o Python inferiu ao valor
print(type('Gabriel'))
print(type(0))
print(type(1.1), type(-1.1), type(0.0))

